public class uwu {
	
	public static void main (String[] args) {
		System.out.println("uwu");
		int uwu = IBIO.inputInt("uwu? ");
		for(int i = 0; i < uwu; i++)
		System.out.print("uwu ");
		System.out.println("");
		System.out.println("");
			System.out.println("press: [1] for uwu");
			System.out.println("       [2] for UWU");
			System.out.println("       [3] for owo");
			System.out.println("");
			int type = IBIO.inputInt("");
		
		switch(type)
		{	case 1:
			for(int x = 0; x < 200; x++)
			System.out.print("uwu ");
			break;
			
			case 2:
			for(int y = 0; y < 200; y++)
			System.out.print("UWU ");
			break;
			
			case 3:
			for(int z = 0; z < 200; z++)
			System.out.print("owo ");
			break;
			
			default:
			for(int y = 0; y < 100000000; y++)
			System.out.print("uwu UWU owo ");
			}
		
	}
}

